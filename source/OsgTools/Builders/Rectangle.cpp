///////////////////////////////////////////////////////////////////////////////
//
//  Class for building a rectangle.
//
///////////////////////////////////////////////////////////////////////////////

#include "OsgTools/Builders/Rectangle.h"
#include "OsgTools/Builders/Options.h"

#include "osg/Geode"
#include "osg/Group"
#include "osg/Material"
#include "osg/PolygonMode"
#include "osg/ref_ptr"
#include "osg/ShapeDrawable"


namespace OsgTools {
namespace Builders {


///////////////////////////////////////////////////////////////////////////////
//
//  Constructor.
//
///////////////////////////////////////////////////////////////////////////////

Rectangle::Rectangle() : BaseClass(),
  _height ( 1.0 ),
  _width ( 1.0 )
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Constructor.
//
///////////////////////////////////////////////////////////////////////////////

Rectangle::Rectangle ( const Options &options ) : BaseClass ( options ),
  _height ( Builders::getNumber ( options, "height", 1.0 ) ),
  _width ( Builders::getNumber ( options, "width", 1.0 ) )
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Destructor.
//
///////////////////////////////////////////////////////////////////////////////

Rectangle::~Rectangle()
{
}


///////////////////////////////////////////////////////////////////////////////
//
//  Build a sphere.
//
///////////////////////////////////////////////////////////////////////////////

osg::Node *Rectangle::build()
{
  osg::ref_ptr < osg::Geode > geode ( new osg::Geode() );
  return geode.release();
}


///////////////////////////////////////////////////////////////////////////////
//
//  Build a sphere.
//
///////////////////////////////////////////////////////////////////////////////

osg::Node *Rectangle::build ( const Options &options )
{
  Rectangle builder ( options );
  return builder.build();
}


///////////////////////////////////////////////////////////////////////////////
//
//  Set the height.
//
///////////////////////////////////////////////////////////////////////////////

void Rectangle::setHeight ( double height )
{
  _height = height;
}
void Rectangle::setHeight ( float height )
{
  _height = height;
}


///////////////////////////////////////////////////////////////////////////////
//
//  Set the width.
//
///////////////////////////////////////////////////////////////////////////////

void Rectangle::setWidth ( double width )
{
  _width = width;
}
void Rectangle::setWidth ( float width )
{
  _width = width;
}


} // namespace Builders
} // namespace OsgTools
